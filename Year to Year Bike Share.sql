-- 3. Identify underperforming seasons (rider count below 25th percentile).

WITH DAILY_TOTALS AS(
SELECT SEASON, DTEDAY, SUM(RIDERS) AS DAILY_RIDER
FROM (
SELECT * FROM BIKE_0
UNION ALL
SELECT * FROM BIKE_1
) AS COMBINED
GROUP BY SEASON, DTEDAY
),
RANKED AS(
SELECT DISTINCT SEASON, DTEDAY, DAILY_RIDER,
ROW_NUMBER() OVER(PARTITION BY SEASON ORDER BY DAILY_RIDER) AS RN,
COUNT(*) OVER(PARTITION BY SEASON) AS TOTAL_DAYS
FROM DAILY_TOTALS
),
SEASONS_THRESHOLD AS(
SELECT SEASON, MIN(DAILY_RIDER) AS PCT_25_VALUE
FROM RANKED
WHERE RN >= FLOOR(0.25 * TOTAL_DAYS)
AND RN <= CEIL(0.25 * TOTAL_DAYS)
GROUP BY SEASON 
)
SELECT dt.SEASON, dt.DTEDAY, dt.DAILY_RIDER, st.PCT_25_VALUE AS SEASON_25TH_PERCENTILE
FROM DAILY_TOTALS dt
JOIN SEASONS_THRESHOLD st
ON dt.SEASON = st.SEASON
WHERE dt.DAILY_RIDER < st.PCT_25_VALUE
ORDER BY dt.DTEDAY, dt.SEASON;


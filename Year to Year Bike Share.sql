-- 5. Scenario-based Analysis
-- 1. Estimate the impact on profit if price increases by 10% without changing riders.

WITH MERGED AS(
SELECT b.YR, SUM(b.RIDERS) AS TOTAL_RIDER, ct.PRICE, ct.COGS
FROM (
SELECT * FROM BIKE_0
UNION ALL
SELECT * FROM BIKE_1
) AS b
JOIN COST_TABLE ct
ON b.YR = ct.YR
GROUP BY b.YR, ct.PRICE, ct.COGS
), 
PROFIT_CAL AS (
SELECT YR, TOTAL_RIDER, PRICE, COGS, TOTAL_RIDER - (PRICE * COGS) AS CURRENT_PROFIT,
(TOTAL_RIDER - (PRICE * 1.10) - COGS) AS NEW_PROFIT
FROM MERGED
)
SELECT YR, CURRENT_PROFIT, NEW_PROFIT, ROUND(NEW_PROFIT - CURRENT_PROFIT,2) AS PROFIT_CHANGE,
ROUND(((NEW_PROFIT - CURRENT_PROFIT) / NEW_PROFIT)*100,2) AS PCT_CHANGE
FROM PROFIT_CAL;


-- 5. Determine rider elasticity — % change in riders per % change in price (approximation)

WITH COMBINED_RIDER AS(
SELECT YR, SUM(RIDERS) AS TOTAL_RIDERS
FROM (
SELECT YR, RIDERS
FROM BIKE_0
UNION ALL
SELECT YR, RIDERS
FROM BIKE_1
) AS COMBINED 
GROUP BY YR
),
JOINED_DATA AS(
SELECT cr.YR, cr.TOTAL_RIDERS, ct.PRICE
FROM COMBINED_RIDER cr
JOIN COST_TABLE ct
ON cr.YR = ct.YR
),
CHANGES AS (
SELECT YR, TOTAL_RIDERS, PRICE
(TOTAL_RIDERS - LAG(TOTAL_RIDERS) OVER(ORDER BY YR)) / LAG(TOTAL_RIDERS) OVER(ORDER BY YR) * 100 AS PCT_CHANGE_RIDERS,
(PRICE - LAG(PRICE) OVER (ORDER BY YR)) / LAG(PRICE) OVER(ORDER BY YR) * 100 AS PCT_CHANGE_PRICE
FROM JOINED_DATA
)
SELECT YR, TOTAL_RIDERS, PRICE, PCT_CHANGE_RIDERS, PCT_CHANGE_PRICE,
CASE 
	WHEN PCT_CHANGE_PRICE != 0 THEN PCT_CHANGE_RIDERS / PCT_CHANGE_PRICE
    ELSE NULL
    END AS ELASTICITY
FROM CHANGES
ORDER BY YR;
